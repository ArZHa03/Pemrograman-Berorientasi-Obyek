public class RerataNilai {
    public int average(int numberOne, int numberTwo) {
        return numberOne + numberTwo / 2;
    }
    public double average(double numberOne, double numberTwo) {
        return numberOne + numberTwo / 2;
    }
    public int average(int numberOne, int numberTwo, int numberThree) {
        return numberOne + numberTwo + numberThree / 3;
    }
}
